/* 輔助函式：將1個byte的字型資料(8 pixels) 轉換為 OLED 格式並發送 */
/* SSD1322 一個像素 4 bits，所以 8 個像素需要 4 個 bytes 發送 */
static void Send_Font_Byte(uint8_t font_data)
{
    uint8_t bit_mask = 0x80;
    uint8_t out_data;
    int k;
    
    // 每次處理兩個 bit (兩個像素)，組成一個 byte (0xF0, 0x0F, 0xFF, 0x00)
    for(k = 0; k < 4; k++) 
    {
        out_data = 0x00;
        // 左邊像素 (高4位)
        if (font_data & bit_mask) out_data |= 0xF0;
        bit_mask >>= 1;
        
        // 右邊像素 (低4位)
        if (font_data & bit_mask) out_data |= 0x0F;
        bit_mask >>= 1;
        
        vWriteData(out_data);
    }
}


void vPrintLogNumber(char *str,...)
{
	uint8_t i,j;
	char word[11] = {0,};
	
	va_list argptr;
	va_start(argptr, str);
	vsprintf(word, str, argptr); // 注意：需確保字串不超過10個字，否則溢出
	va_end(argptr);
	
	for(i = 0; i < 10; i++)
	{
		if(word[i] == 0) return;
		
        // 設定寫入視窗 (Column)
		vWriteCommand(0x15);   
		vWriteData(0x1c  + i*4);
		vWriteData(0x1c + i*4 + 3);
		vWriteCommand(0x75);   // set raw address
		vWriteData(00);
		vWriteData(0x3f);
		vWriteCommand(0x5c);   // write ram command
		
		for(j = 0; j < 64; j+=1)
		{
            if(word[i] == ' ') {
                // 空白字元直接填 0 (需填入4個byte因為原本邏輯是展開8個bit)
                vWriteData(0x00); vWriteData(0x00); vWriteData(0x00); vWriteData(0x00);
            } else {
                // 使用封裝好的函式發送資料，邏輯更清晰，減少重複代碼
                Send_Font_Byte(font_16x32[(word[i] - 0x30) * 64 + j]);
            }
		}
	}
}

