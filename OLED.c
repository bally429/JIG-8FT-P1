// =========================================================================
void vPrintAsciiRaw(int rawStr, char *str, ...)
{
    uint8_t i, j;
    // [防護1] 開 40 Bytes 的陣列，保證 32 字元的螢幕絕對裝得下
    char word[40] = {0};
    
    va_list argptr;
    va_start(argptr, str);
    // [防護2] 使用 vsnprintf 取代 vsprintf，限制最多只寫入 39 個字元，澈底杜絕記憶體溢位當機！
    vsnprintf(word, sizeof(word) - 1, str, argptr);
    va_end(argptr);
    
    for(i = 0; i < 32; i++) // 螢幕最寬 256 pixels / 8 = 32 個字元
    {
        char c = word[i];
        if(c == 0) c = ' '; // 自動補空白，把螢幕上舊的殘影清乾淨
        if(c < 0x20 || c > 0x7E) c = ' '; // [防護3] 過濾不合法的亂碼，防止字型陣列越界
        
        vWriteCommand(0x15);   // set column address
        vWriteData(0x1c + i*2);
        vWriteData(0x1c + i*2 + 1);
        vWriteCommand(0x75);   // set raw address
        vWriteData(rawStr);
        vWriteData(0xff);
        vWriteCommand(0x5c);   // write ram command
        
        for(j = 0; j < 16; j+=1)
        {
            Send_Font_Byte(font_8x16[((c - 0x20) * 16) + j]);
        }
    }
}

