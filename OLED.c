// 2. ®Ö¤ßµeÂI¨çĽĆ (±aµwĹéĂä¬É«OĹ@)
void OLED_DrawPixel(int x, int y, uint8_t color) {
    if(x < 0 || x > 255 || y < 0 || y > 63) return; // «OĹ@ˇGµ´ąď¤ŁµeĄX¬É
    
    // SSD1322 ¤@­Ó Byte ¸Ë¨â­ÓąłŻŔ (°Ş 4 bit ¬°°¸ĽĆ XˇA§C 4 bit ¬°©_ĽĆ X)
    if(x % 2 == 0) OLED_GRAM[y][x/2] = (OLED_GRAM[y][x/2] & 0x0F) | (color << 4);
    else           OLED_GRAM[y][x/2] = (OLED_GRAM[y][x/2] & 0xF0) | (color & 0x0F);
}

extern const unsigned char font_8x16[]; 

// 3. ±a¤W¤UĂä¬Éµô¤ÁŞş¦r¤¸Ă¸»s
void OLED_DrawChar(int x, int y, int min_y, int max_y, char c, uint8_t brightness) {
    if(c < 0x20 || c > 0x7E) return;
    const unsigned char *glyph = &font_8x16[(c - 0x20) * 16];
    
    for(int r = 0; r < 16; r++) {
        int draw_y = y + r;
        if (draw_y < min_y || draw_y > max_y) continue; // łnĹéµô¤ÁˇG§ą¬üąę˛{¤ĺ¦r·Ć¤J/·ĆĄXŻS®Ä
        
        uint8_t line = glyph[r];
        for(int col = 0; col < 8; col++) {
            if(line & (0x80 >> col)) OLED_DrawPixel(x + col, draw_y, brightness);
            else OLED_DrawPixel(x + col, draw_y, 0x00); // ÂĐ»\­I´ş¨ľŻ}ąĎ
        }
    }
}

// 4. ¦r¦ęĂ¸»s¤ŢŔş
void OLED_PrintString(int x, int y, int min_y, int max_y, const char *str, uint8_t brightness) {
    int curr_x = x;
    while(*str) {
        OLED_DrawChar(curr_x, y, min_y, max_y, *str, brightness);
        curr_x += 8;
        str++;
    }
}

// 5. ±N 8KB Ĺă¦s¤@Şi¬yĹF¬µ¨ě OLED (·Ąłt¤Ł°{«Ě)
void OLED_Update(void) {
    vWriteCommand(0x15); vWriteData(0x1C); vWriteData(0x5B); // ĄţĽe«×
    vWriteCommand(0x75); vWriteData(0x00); vWriteData(0x3F); // Ąţ°Ş«×
    vWriteCommand(0x5C); // ¶}±ŇĽg¤J
    
    OLED_DATAMODE;
    for(int r = 0; r < 64; r++) {
        for(int c = 0; c < 128; c++) {
            WAIT_SPI_IDLE(SPI0); 
            SPI_WRITE_TX(SPI0, OLED_GRAM[r][c]); // Ş˝±µ¶ë¶iµwĹé FIFO
        }
    }
    WAIT_SPI_IDLE(SPI0);
}

